    private void btnAddClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddClienteActionPerformed

        if(txtID.getText().equals("") || txtNome.getText().equals("") || mskValorTotal.getText().equals("") ||
                (rdbCNPJ.isSelected() && mskCNPJ.getText().equals("")) || (rdbCpf.isSelected() && mskCPF.getText().equals(""))){
            JOptionPane.showMessageDialog(null,
                "Informe todos os campos antes de cadastrar o cliente");
        }else{
            if(!daocustomer.idLivre(Integer.parseInt(txtID.getText()))){
                JOptionPane.showMessageDialog(null,
                    "Valor do identificador jÃ¡ registrado");
                txtID.requestFocus();
            }else{
                String cpfcnpj;
                if(rdbCNPJ.isSelected()){
                    cpfcnpj = mskCNPJ.getText().replace("-", "").replace("/", "");
                }else{
                    cpfcnpj = mskCPF.getText().replace("-", "").replace("/", "");
                }
                Double valor = Double.parseDouble(mskValorTotal.getText().replaceAll("\\.","").replace(",","."));
                customer = new Customer(Integer.parseInt(txtID.getText()), cpfcnpj,
                  txtNome.getText(), rdbContaAtiva.isSelected(), valor);
                daocustomer.inserir(customer);
                btnLimparActionPerformed(evt);
                gerarMediaFinal();
            }
        }
    }//GEN-LAST:event_btnAddClienteActionPerformed

