    public void gerarMediaFinal(){
        
       Double media = 0.0; 
        
       arrayCustomer = daocustomer.getALL();  
       
       for(int i=0; i<arrayCustomer.size(); i++){
           if(arrayCustomer.get(i).getValor_total() <= 560 || 
                   arrayCustomer.get(i).getId() < 1500 ||
                   arrayCustomer.get(i).getId() > 2700){
               arrayCustomer.remove(i);
           }
       }       
       
       modTblCustomers.setNumRows(0);
       Collections.sort(arrayCustomer, new ComparadorDeCustomer());
       
        for (Customer customer1 : arrayCustomer) {
            String linha[] = {String.valueOf(customer1.getId()),
                            customer1.getNome(), 
                            String.valueOf(df.format(customer1.getValor_total()))};
            modTblCustomers.addRow(linha);
            media += customer1.getValor_total();
        }
        
        if(media == 0){
            lblMedia.setText("0.00");
        }else{
            lblMedia.setText(df.format(media / arrayCustomer.size()));
        }
    }
    
